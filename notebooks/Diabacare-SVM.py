import numpy as np
import pandas as pd
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split
from sklearn import svm
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.metrics import accuracy_score

"""Collection of Data and its analysis
https://www.kaggle.com/kumargh/pimaindiansdiabetescsv
"""

# loading the MainDiabetes dataset to a pandas DataFrame
maindiabetes_dataset = pd.read_csv('/content/MainDiabetes.csv')

# print 5 rows of the dataset
maindiabetes_dataset.head()

# number of rows and columns in the dataset
maindiabetes_dataset.shape

# statistical measures of the data
maindiabetes_dataset.describe()

maindiabetes_dataset ['Outcome'].value_counts()

"""0 represents Non Diabetics.
1 represents DIabetic
"""

maindiabetes_dataset.groupby('Outcome').mean()

"""Removing the "Outcome" column from the data"""

# create new label X, axis is valued 1 to drop column, and 0 to drop row
X = maindiabetes_dataset.drop(columns = 'Outcome', axis = 1)
Y = maindiabetes_dataset[ 'Outcome']

# printing the outcome
print(X)

print(Y)

"""DATA STANDARDIZATION """

scaler = StandardScaler()

scaler.fit(X)

standardized_data = scaler.transform(X)

print(standardized_data)

X = standardized_data
Y = maindiabetes_dataset['Outcome']

print(X)

print(Y)

"""Splitting the dataset into TRAINING DATA and TESTING DATA"""

X_train, X_test, Y_train, Y_test = train_test_split(X,Y, test_size=0.2, stratify=Y, random_state=2)

print(X.shape, X_train.shape, X_test.shape)

"""TRAINING THE SUPPORT VECTOR MACHINES (SVM)"""

classifier =svm.SVC(kernel='linear')

# Training SVM
classifier.fit(X_train, Y_train)

"""EVALUATING SVM"""

# Accuracy score of training data
X_train_prediction = classifier.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, Y_train)

print('Accuracy score of the training data : ', training_data_accuracy)

# Accuracy score of test data
X_test_prediction = classifier.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, Y_test)

#@title
print('Accuracy score of the test data : ', test_data_accuracy)

