# Commented out IPython magic to ensure Python compatibility.
import math
import numpy as np
import pandas as pd
from datetime import datetime

import seaborn as sns
import matplotlib.pyplot as plt
# %matplotlib inline 
plt.style.use('seaborn-whitegrid')

from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import classification_report
from sklearn.metrics import confusion_matrix

maindiabetes_dataset = pd.read_csv('/content/MainDiabetes.csv')

X = maindiabetes_dataset.drop(columns = 'Outcome', axis = 1)
Y = maindiabetes_dataset[ 'Outcome']

from sklearn.model_selection import train_test_split

X_train, X_test, Y_train, Y_test = train_test_split(X,Y, test_size=0.2, stratify=Y, random_state=2)

rfm = RandomForestClassifier(n_estimators=70, oob_score=True, n_jobs=-1, random_state=101, max_features = None, min_samples_leaf = 30)
rfm.fit(X_train, Y_train)
Y_pred=rfm.predict(X_test)
print(Y_pred)

from sklearn import metrics

# Model Accuracy

print("Accuracy:",metrics.accuracy_score(Y_test, Y_pred))

