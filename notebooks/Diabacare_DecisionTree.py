import pandas as pd
import seaborn as sns
from sklearn.tree import DecisionTreeClassifier
from sklearn import metrics
from sklearn.model_selection import train_test_split

# loading the MainDiabetes dataset to a pandas DataFrame
maindiabetes_dataset = pd.read_csv('/content/MainDiabetes.csv')

# print 5 rows of the dataset
maindiabetes_dataset.head()

X = maindiabetes_dataset.drop(columns = 'Outcome', axis = 1)
Y = maindiabetes_dataset[ 'Outcome']

from sklearn.model_selection import train_test_split

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, random_state=1)

# Instantiate a DecisionTreeClassifier 'dt' with a maximum depth of 6
dt = DecisionTreeClassifier(criterion='entropy', max_depth=3) 

# Fit dt to the training set
dt = dt.fit(X_train, Y_train)

# Predict test set labels
Y_pred = dt.predict(X_test)
print("Accuracy is :", metrics.accuracy_score(Y_test, Y_pred))

